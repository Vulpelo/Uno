class Vector2d {
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }
    get getX() {
        return this.x;
    }
    set setX(x) {
        this.x = x;
    }
    get getY() {
        return this.x;
    }
    set setY(x) {
        this.x = x;
    }
    coordinate(x, y) {
        this.x = x;
        this.y = y;
    }
}
